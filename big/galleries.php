    <h2>Overview</h2>
    <p>When done well, galleries can give your website that extra visual appeal that keeps them browsing longer or coming back; When done poorly, they can frustrate or turn off potential users. With these resources and tips, you can ensure your galleries add value, not detract.</p>

<p>First, a little review of concepts for those who might be new or unfamiliar. Knowing the basic ideas behind all the buzz can help you make better informed choices when talking about your design ideas and dreams.</p>

<p><strong>Gallery:</strong> a streamlined way to organize a group of images on a webpage for viewing. These are commonly pictures, but they could be videos, pdfs, etc. Really, any similar items you’d want to group depending on the context. For example, a photography website might have a gallery page of their work whereas a school page might have a gallery setup with study terms and practice cards. The applications are limited only by your imagination (and good design practices).</p>

<p><strong>Lightbox:</strong> Scripts that add extra function to galleries. When the user selects an image, it is usually brought front and center with the rest of the website going dark or soft focus to better highlight the image. A professional and visually appealing addition to any gallery. In cases of a lack of support, the script will not run and instead the image will usually be brought up in it’s own page (or whichever back-up response the programmer has set-up).</p>
<p>Things to keep in mind when designing your gallery:</p>
<ul class="research">
    <li>Where will it go? How will users find it?
        Does it have its own page? Will it be set off to the side/top/bottom and visible from all pages?
    </li>
    <br>
    <li>What is the purpose? Is it to inform? To show off your work? To display lots of items for sale?
        <ul>
            <li>Showing off artistic work you might choose a gallery that goes for form over function, showing fewer images at once and closing in on them for a more dramatic display. Whereas for a gallery of items for sale, you might prefer allowing users to scroll through more images quickly and a more simplistic focus function for faster, easier browsing.</li>
        </ul>
        <br>
    <li>Keep it simple. A gallery is something that can easily be overdesigned adding clutter and access information ruining the visual appeal. </li>
    <br>
    <li>Browse through some galleries you like and see what they do well. Take notes on what features you like and dislike.</li>
</ul>
